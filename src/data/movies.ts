export const generateCatalog = (): Movie[] => {
  return Array.from({ length: 490 }, (_, i) => {
    const id = 100 + i;
    const titleIdx = i % scifiTitles.length;
    const suffix = Math.floor(i / scifiTitles.length) > 0 ? ` ${Math.floor(i / scifiTitles.length) + 1}` : '';
    const seed = posterSeeds[i % posterSeeds.length];
    const uniqueId = i * 7 + 100;
    return {
      id,
      title: scifiTitles[titleIdx] + suffix,
      year: 1990 + (i % 35),
      genre: genres[i % genres.length],
      rating: parseFloat((6.5 + (i % 30) * 0.1).toFixed(1)),
      duration: `${1 + (i % 2)}ч ${10 + (i % 50)}м`,
      poster: `https://picsum.photos/seed/${seed}-${uniqueId}/300/450`,
      description: `Захватывающий научно-фантастический фильм с непредсказуемым сюжетом и великолепными спецэффектами. Режиссёр: ${directors[i % directors.length]}.`,
    };
  });
};

export const catalogMovies: Movie[] = generateCatalog();
