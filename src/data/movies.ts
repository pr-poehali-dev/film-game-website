function seededRandom(seed: number): number {
  const x = Math.sin(seed + 1) * 10000;
  return x - Math.floor(x);
}

export function generateCatalog(count = 10000): Movie[] {
  const movies: Movie[] = [];

  for (let i = 0; i < count; i++) {
    const id = 1000 + i;
    const r = (offset: number) => seededRandom(id * 31 + offset);

    // Title generation — 4 strategies
    let title: string;
    const strategy = i % 4;
    if (strategy === 0) {
      title = `${ADJECTIVES[Math.floor(r(1) * ADJECTIVES.length)]} ${NOUNS[Math.floor(r(2) * NOUNS.length)]}`;
    } else if (strategy === 1) {
      title = `${PREFIXES[Math.floor(r(3) * PREFIXES.length)]} «${NOUNS[Math.floor(r(4) * NOUNS.length)]}»`;
    } else if (strategy === 2) {
      title = `${NOUNS[Math.floor(r(5) * NOUNS.length)]}: ${SUBTITLES[Math.floor(r(6) * SUBTITLES.length)]}`;
    } else {
      title = `${ADJECTIVES[Math.floor(r(7) * ADJECTIVES.length)]} ${NOUNS[Math.floor(r(8) * NOUNS.length)]}: ${SUBTITLES[Math.floor(r(9) * SUBTITLES.length)]}`;
    }

    const year = 1975 + Math.floor(r(10) * 50);
    const ratingBase = 5.5 + r(11) * 4.0;
    const rating = parseFloat(ratingBase.toFixed(1));
    const hours = 1 + Math.floor(r(12) * 2);
    const minutes = 5 + Math.floor(r(13) * 55);
    const director = DIRECTORS[Math.floor(r(14) * DIRECTORS.length)];
    const description = DESCRIPTIONS[Math.floor(r(15) * DESCRIPTIONS.length)];
    const keyword = POSTER_KEYWORDS[Math.floor(r(16) * POSTER_KEYWORDS.length)];
    const posterSeed = `${keyword}-${id}`;

    movies.push({
      id,
      title,
      year,
      genre: "Фантастика",
      rating,
      duration: `${hours}ч ${minutes}м`,
      poster: `https://picsum.photos/seed/${posterSeed}/300/450`,
      description: `${description} Режиссёр: ${director}.`,
    });
  }

  return movies;
}

export const catalogMovies: Movie[] = generateCatalog(10000);
